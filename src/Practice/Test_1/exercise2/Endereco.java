public class Endereco {
    String cidade;
    String estado;
    String pais;
    String logradouro;
    
    public Endereco(String cidade, String estado, String pais, String logradouro) {
        this.cidade = cidade;
        this.estado = estado;
        this.pais = pais;
        this.logradouro = logradouro;
    }
}
